import UIKit

extension UIColor {
	class func mainBackgroundColor() -> UIColor {
		return UIColor(red:44.0/255.0, green:62.0/255.0, blue:88.0/255.0, alpha:1.0)
	}

	class func navBarBackgroundColor() -> UIColor {
		return UIColor(red:52.0/255.0, green:73.0/255.0, blue:94.0/255.0, alpha:1.0)
	}
	
	class func textColor() -> UIColor {
		return UIColor(red:236.0/255.0, green:240.0/255.0, blue:241.0/255.0, alpha:1.0)
	}
}
